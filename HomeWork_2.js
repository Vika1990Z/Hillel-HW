// Как тебя зовут? Какого ты года рождения? Выводится сообщение с именем и годом. Например, если пользователь ответил "Сергей" и "1979" то выводится "Сергей 1979"

let userName = prompt('Как тебя зовут?');
let userBirth = prompt ('Какого ты года рождения?');

console.log(`${userName} ${userBirth}`); // вывод в консоль
alert(`${userName} ${userBirth}`); // вывод в браузер 



